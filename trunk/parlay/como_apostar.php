<h2>Apuesta por Derecho</h2>
<p>Se divide en tres tipos de apuestas:</p>
<br />
<ol>
  <li>Al equipo o los puntos (dar o recibir)</li>
  <li>Altas o Bajas</li>
  <li>Moneyline</li>
</ol>
<br />
<h2>Apuestas Futuras</h2>
<br />
<p>Estas apuestas son a largo plazo, como por ejemplo, ganar la Serie Mundial de béisbol, el Superbowl, la Stanley Cup, etc., estos logros cambian en la medida que la temporada avanza, pero el logro valido es aquel del momento en que el cliente hace la apuesta.</p>
<br />
<h2>Parlays</h2>
<p>Un parlay es una combinación de dos o más opciones de juego. Usted debe acertar correctamente todas las opciones que escoja. Mientras más opciones se escojan, mayor será el monto que ganara, por cuanto el premio de la primera apuesta será el monto a apostar de la siguiente y así sucesivamente. Se pueden hacer parlays entre diferentes deportes.</p>
<br />
